    private void ID() {
        try {
            ResultSet rs;
            DatabaseFunction db = new DatabaseFunction();
            Connection con = db.getconnection();
            String s = "select * from projects";
            rs = db.combobox(s);
            while (rs.next()) {
                Project_ID.addItem(rs.getString("project_id"));
            }
        } catch (Exception e) {
            System.out.println("SQL Exception: " + e.toString());

        }
    }

    private void employee() {
        try {
            ResultSet rs;
            DatabaseFunction db = new DatabaseFunction();
            Connection con = db.getconnection();
            String s = "select * from employee";
            rs = db.combobox(s);
            while (rs.next()) {
                assignedfor.addItem(rs.getString("username"));
            }
        } catch (Exception e) {
            System.out.println("SQL Exception: " + e.toString());

        }
    }

