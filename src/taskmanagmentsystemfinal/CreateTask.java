    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {

            String code1 = code.getText();
            String title1 = title.getText();
            String description1 = description.getText();
            String assignedfor1 = assignedfor.getSelectedItem().toString();
            String phase1 = phase.getText();
            String projectid1 = Project_ID.getSelectedItem().toString();
            String priority1 = priority.getText();
            String creatorname1 = creatorname.getText();
            String start1 = day1.getSelectedItem().toString() + "/" + m1.getSelectedItem().toString() + "/" + year1.getSelectedItem().toString();
            String end1 = day2.getSelectedItem().toString() + "/" + m2.getSelectedItem().toString() + "/" + year2.getSelectedItem().toString();
            String estimation1 = estimation.getText();

            if (!code1.isEmpty() && !title1.isEmpty() && !description1.isEmpty() && !assignedfor1.isEmpty()
                    && !phase1.isEmpty() && !projectid1.isEmpty() && !priority1.isEmpty() && !creatorname1.isEmpty()
                    && !estimation1.isEmpty()) {
                Database db = new Database();
                if (db.CreateTask(code1, title1, description1, assignedfor1, phase1, projectid1, priority1, creatorname1, start1, end1, estimation1)) {
                    JOptionPane.showMessageDialog(null, "inserted");
                    backPress();
                } else {
                    JOptionPane.showMessageDialog(null, "error");
                }
            } else {
                JOptionPane.showMessageDialog(null, "You must enter all fields");
            }

        } catch (Exception e) {
            System.out.println("SQL Exception: " + e.toString());

        }
    }//GEN-LAST:event_jButton1ActionPerformed

